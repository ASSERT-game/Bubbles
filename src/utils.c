int		square_distance(int x, int y, int to_x, int to_y)
{
	int	result;
	int	d_x;
	int	d_y;

	d_x = (to_x - x);
	d_y = (to_y - y);
	result = d_x * d_x + d_y * d_y;
	return (result);
}
